#include<stdio.h>

int main()
{
    // positive.
    int a = 9;
   
    // negative.
    int b = -9;
   
    // unsigned int -  U/u.
    int c = 89U;
   
    // long int - L/l.
    long int d = 99998L;
 
    printf("positive: %d\n", a);
    printf("negative: %d\n", b);
    printf("unsigned int: %u\n", c);
    printf("long int: %ld", d);
 
    return 0;
}