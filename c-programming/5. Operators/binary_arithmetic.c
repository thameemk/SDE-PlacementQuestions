#include <stdio.h>
  
int main()
{
    int a = 10, b = 4;
  
    printf("a%d\n", a);
    printf("b%d\n", b);

    // addition
    printf("a + b %d\n", a + b);
  
    // subtraction
    printf("a - b %d\n", a-b);
  
    // multiplication
    printf("a * b %d\n", a*b);
  
    // division
    printf("a / b %d\n", a/b);
  
    // modulus
    printf("a %% b %d\n", a%b);
  
    return 0;
}